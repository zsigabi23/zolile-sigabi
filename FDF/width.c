#include "fdf.h"

int		width(char **map, int i)
{
	int		j;
	int		z;

	j = 0;
	z = 0;
	while (map[i][j] != '\0')
	{
		if (ft_isdigit(map[i][j]) == 1 && ft_isdigit(map[i][j + 1]) != 1)
		{
			z++;
			while (map[i][j] != ' ' && map[i][j] != '\0')
				j++;
		}
		else
			j++;
	}
	return (z);
}
